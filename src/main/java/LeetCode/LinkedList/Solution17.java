package LeetCode.LinkedList;

import LeetCode.LinkedList.ListNode;

public class Solution17 {
    public ListNode deleteDuplicates(ListNode head) {
        if (head == null) {
            return head;
        }
        ListNode cur = head;
        while (cur.next != null) {
            //因为有序，所以相同的都是连续的
            if (cur.val == cur.next.val) {
                cur.next = cur.next.next;
            } else {
                cur = cur.next;
            }
        }
        return head;
    }
}
